async function main() {
  console.log('Iniciando importação de produtos...')

  // Remover dados que dependem de produtos (na ordem correta das foreign keys)
  console.log('Limpando dados dependentes...')
  await prisma.utensilCountItem.deleteMany()
  await prisma.labelTemplate.deleteMany()
  await prisma.label.deleteMany()
  await prisma.recipeIngredient.deleteMany()
  await prisma.productionOrderIngredient.deleteMany()
  await prisma.productionOrder.deleteMany()
  await prisma.recipe.deleteMany()
  await prisma.purchaseRequest.deleteMany()
  await prisma.purchaseCycle.deleteMany()
  await prisma.stockEntry.deleteMany()
  await prisma.stockBalance.deleteMany()
  await prisma.product.deleteMany()

  console.log('Importando produtos...')
  let count = 0
  for (const [codigoCotacao, nome, unidadeMedida] of PRODUTOS_CSV) {
    await prisma.product.create({
      data: {
        nome,
        codigoCotacao,
        unidadeMedida: unidadeMedida.toLowerCase(),
        participaCotacao: true,
        status: 'ativo',
      },
    })
    count++
  }

  console.log(`✓ ${count} produtos importados com sucesso!`)
}

main()
  .catch((e) => {
    console.error(e)
    process.exit(1)
  })
  .finally(async () => {
    await prisma.$disconnect()
  })
